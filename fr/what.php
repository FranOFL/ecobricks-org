                <p data-lang-id="024-block-2-main-p1">Les écobriques normales sont fabriquées avec n'importe quelle taille de bouteille en plastique PET transparent. La bouteille et le plastique emballé sont propres et secs pour empêcher la croissance de bactéries. En règle générale, les écobrickers utilisent un bâton en bois ou en bambou pour emballer manuellement le plastique dans la bouteille en plastique. Le plastique est coupé ou déchiré en petits morceaux puis emballé petit à petit, en alternant entre l'ajout du plastique et son compactage, couche par couche. La bouteille est tournée à chaque pression pour s'assurer que le plastique est uniformément compacté dans toute la bouteille. Cela aide à éviter les vides et à ce que la garniture atteigne la solidité requise pour les applications d'un bloc de construction.</p>
