                <div class="opener-header">
                    
                    <div class="opener-header-text">
                    <h4>How it works</h4>
                    <h5>Brikcoins are generated and verified by human labour.</h5>   
                    <br>
                    </div>

                    <button onclick="preclosed3()" class="block-toggle" id="block-toggle-show3">+</button>
