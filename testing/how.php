				<h4>9. Inscribe</h2>
				<h5>As you log and record your ecobrick, be sure to record the key data on the ecobrick itself too. </h5>   
			</div>
			<button onclick="preclosed9()" class="block-toggle" id="block-toggle-show9">+</button>
